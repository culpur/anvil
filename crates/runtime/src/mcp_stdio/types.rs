// ── Pagination helper ───────────────────────────────────────────────────────
//
// Task #715: per the MCP spec the server signals "no more pages" by either
// omitting `nextCursor` or returning it as an empty / whitespace-only string.
// A naive `match cursor { Some(_) => continue, None => break }` loops forever
// when the server returns `Some("")` — drive every paginated list through
// this helper instead. Returns `Some(next)` only when `next` is a non-empty
// cursor string.
#[must_use]
pub fn advance_pagination_cursor(next_cursor: Option<String>) -> Option<String> {
    let next = next_cursor?;
    if next.trim().is_empty() {
        None
    } else {
        Some(next)
    }
}
